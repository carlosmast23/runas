@section('contenido')    
La mamografía es hasta el momento el mejor estudio para la detección temprana de cáncer de mama. Es un examen médico no invasivo, que es una pequeña dosis de radiación ionizante. El estudio no requiere la protección del cuello, sino que es un momento para expandir a través de las redes sociales. Sin embargo, en caso de solicitarlo, nosotros le proporcionaremos la protección requerida. La mamografía realizada no ha sido irradiada por los tratamientos de cáncer, no hay riesgos demostrados y que la radiación es mínima. Al contrario, se ha evidenciado que el uso de protectores de tiroides entorpece el examen y puede afectar la visualización y el buen diagnóstico. La respuesta del paciente en su pecho se debe a la compresión, la necesidad, (de pocos segundos), de sus mamas con una paleta que contiene el equipo. Algunas mujeres con sentimientos sensibles pueden experimentar incomodidad. Se recomienda que el paciente se encuentre desodorado en las axilas.
@endsection

