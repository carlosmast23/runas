@section('contenido')    
La ecografía es un estudio diagnóstico que utiliza las ondas de ultrasonido para las partes del cuerpo, como por ejemplo el hígado y las vías biliares, el riñón, el útero y los ovarios, las arterias carotídeas, las venas de los Brazos y piernas, las mamas, los músculos y los tendones, etc. 
Utilizando herramientas del ultrasonido se puede valorar la elasticidad de los nódulos de la mamá y la tiroides, así como también las cicatrices de las lesiones post traumáticas de los músculos y los tendones. 
La ecografía brinda la posibilidad de usar radiación ionizante, biopsias y punciones de mama, próstata, hígado, músculo, etc.
Dentro de los diferentes usos de este mismo método, también, en el estudio 3D y 4D, obstétrico es valioso en la etapa de gestación. 
Es un método inocuo, no doloroso, enteramente dependiente de la persona quien realiza lo que debe practicarse en lugares calificados, con equipos modernos y personal altamente entrenado.@endsection

@section('contenido2')    
El estudio de ecografía tiene dos peculiaridades: La primera es que es un método "no invasivo" que no emplea la radiación ionizante. Por esta razón, puede hacerse a cualquier edad y en mujeres embarazadas. La segunda es que es un método enteramente dependiente de la capacidad de la persona que lo realiza, por lo que debe ser practicado en lugares calificados, con equipos modernos y personal altamente entrenado. El médico especialista que realiza el estudio aplicó sobre su piel un gel acuoso, que no mancha la ropa, y que le servirá para deslizar un pequeño aparato llamado transductor el cual emitirá las ondas de ultrasonido y recogerá las ondas de regreso, llamadas ecos. El software del equipo elabora las imágenes de los órganos del cuerpo y la presentación en la pantalla.
@endsection

@section('contenido3')    
La mayoría de los estudios no requieren una preparación previa y solo algunas indicaciones. Los estudios con carácter urgente no suelen requerir preparación. Es conveniente hacer una cita en la que le proporcionamos las instrucciones para su estudio. El día de la cita con la ropa cómoda y el tiempo disponible. La mayoría de los estudios tienen una duración promedio de 15 a 30 minutos, a partir del momento en que usted entra en la sala de exploración.
@endsection

